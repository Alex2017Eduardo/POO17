public class globales{

	static Byte a = 1;
	static Short b = 0;
	static int c = 1250;
	static long d = 0;
	static Double e = 0.0D;
    static char f = 67;
    static Boolean g = false;
    static String h = null;

	public static void main (String[]args){
		 System.out.println(" ");
		 System.out.println(" ");
		 System.out.println(" VARIABLES GLOBALES INICIALIZADAS");
		 System.out.println(" ");
		 System.out.println(" ");

	System.out.println(" Byte    se inicializo en "+ (a));
	System.out.println(" Short   se inicializo en "+ (b));
	System.out.println(" int     se inicializo en "+ (c));
	System.out.println(" Long    se inicializo en "+ (d));
	System.out.println(" Double  se inicializo en "+ (e));
	System.out.println(" Char    se inicializo con la letra "+ (f));
	System.out.println(" Boolean se inicializo como "+ (g));
	System.out.println(" String  se inicializo como "+ (h));
	}
}